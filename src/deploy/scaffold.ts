/**
 * Generate a deployable Cloudflare Worker bundle from compiled WASM.
 *
 * The bundle contains:
 * - `worker.js` — ES Module Worker entry point with all host imports inline
 * - `program.wasm` — the compiled WASM binary
 * - `wrangler.toml` — Wrangler v3 configuration
 *
 * Host functions in worker.js are derived from the ALL_BUILTINS registry at
 * generation time, matching CloudflareHostAdapter's behavior:
 * - Crypto: returns empty strings (async Workers crypto.subtle unusable in sync)
 * - HTTP: returns structured Result<String,String> Err values
 * - IO: returns structured errors (KV is async)
 * - env: reads from Workers environment bindings
 *
 * @param wasm - Compiled WASM binary (Uint8Array from codegen)
 * @param config - Worker configuration
 * @returns ScaffoldResult with the bundle or an error
 */
export function generateWorkerScaffold(
    wasm: Uint8Array,
    config: WorkerConfig,
): ScaffoldResult {
    // Input validation
    if (!wasm || wasm.length === 0) {
        return { ok: false, error: "wasm_empty" };
    }
    if (!config.name || config.name.trim().length === 0) {
        return { ok: false, error: "name_empty" };
    }

    const files: WorkerBundleFile[] = [
        { path: "worker.js", content: generateWorkerJs() },
        { path: "program.wasm", content: wasm },
        { path: "wrangler.toml", content: generateWranglerToml(config) },
    ];

    return { ok: true, bundle: { files } };
}

/**
 * Get the list of host builtin names included in the generated Worker script.
 * Useful for testing and verification.
 */
export function getHostBuiltinNames(): readonly string[] {
    return HOST_BUILTIN_NAMES;
}
